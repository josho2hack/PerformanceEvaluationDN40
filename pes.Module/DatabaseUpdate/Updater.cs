                defaultRole.AddTypePermission<PointOfEvaluation>(SecurityOperations.Read, SecurityPermissionState.Allow);
                defaultRole.AddTypePermission<SubPointOfEvaluation>(SecurityOperations.Read, SecurityPermissionState.Allow);
