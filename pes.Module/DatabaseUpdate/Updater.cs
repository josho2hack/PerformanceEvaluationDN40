        private PermissionPolicyRole CreateDefaultRole() {
            PermissionPolicyRole defaultRole = ObjectSpace.FindObject<PermissionPolicyRole>(new BinaryOperator("Name", "Default"));
            if(defaultRole == null) {
                defaultRole = ObjectSpace.CreateObject<PermissionPolicyRole>();
                defaultRole.Name = "Default";
                defaultRole.IsAdministrative = false;


                defaultRole.AddTypePermission<MediaDataObject>(SecurityOperations.FullAccess, SecurityPermissionState.Allow);

                defaultRole.AddTypePermission<PermissionPolicyRole>(SecurityOperations.Read, SecurityPermissionState.Allow);

                defaultRole.AddObjectPermission<Employee>(SecurityOperations.FullObjectAccess, "[Oid] = CurrentUserId()", SecurityPermissionState.Allow);
                defaultRole.AddMemberPermission<Employee>(SecurityOperations.Write, "ChangePasswordOnFirstLogon", "[Oid] = CurrentUserId()", SecurityPermissionState.Deny);
                defaultRole.AddMemberPermission<Employee>(SecurityOperations.Write, "StoredPassword", "[Oid] = CurrentUserId()", SecurityPermissionState.Deny);
                defaultRole.AddMemberPermission<Employee>(SecurityOperations.Write, "IsActive", "[Oid] = CurrentUserId()", SecurityPermissionState.Deny);
                defaultRole.AddMemberPermission<Employee>(SecurityOperations.Write, "UserName", "[Oid] = CurrentUserId()", SecurityPermissionState.Deny);

                defaultRole.AddNavigationPermission(@"Application/NavigationItems/Items/User/Items/MyDetails", SecurityPermissionState.Allow);
                defaultRole.AddNavigationPermission(@"Application/NavigationItems/Items/Default/Items/Score_ListView", SecurityPermissionState.Allow);
                defaultRole.AddNavigationPermission(@"Application/NavigationItems/Items/Audit/Items/AuditScore_ListView", SecurityPermissionState.Allow);

                defaultRole.AddTypePermission<Score>(SecurityOperations.Read, SecurityPermissionState.Allow);
                defaultRole.AddObjectPermission<Score>(SecurityOperations.FullObjectAccess, "Owner.Oid = CurrentUserId()", SecurityPermissionState.Allow);
                defaultRole.AddTypePermission<OwnScore>(SecurityOperations.Read, SecurityPermissionState.Allow);
                defaultRole.AddObjectPermission<OwnScore>(SecurityOperations.FullObjectAccess, "Owner.Oid = CurrentUserId()", SecurityPermissionState.Allow);

                defaultRole.AddTypePermission<AuditScore>(SecurityOperations.Read, SecurityPermissionState.Allow);  
                defaultRole.AddTypePermission<Dimension>(SecurityOperations.Read, SecurityPermissionState.Allow);
                defaultRole.AddTypePermission<Evaluation>(SecurityOperations.Read, SecurityPermissionState.Allow);
                defaultRole.AddTypePermission<EvaluationRound>(SecurityOperations.Read, SecurityPermissionState.Allow);
                defaultRole.AddTypePermission<Office>(SecurityOperations.Read, SecurityPermissionState.Allow);
